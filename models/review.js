const mongoose = require('mongoose');

const reviewSchema = new mongoose.Schema({
  clientImage: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  rating: {
    type: Number,
    required: true,
    min: 1,
    max: 5,
  },
});

const Review = mongoose.model('Testimonial', reviewSchema);

module.exports = Review;
